public class AugmentedOperator {
    public static void main(String[] args) {
        double a = 6.5;
        a = a + a + 6;
        System.out.println(a);

        double b = 6.5;
        b = b + 6;
        System.out.println(b);

        double c = 4;
        c /= 2;
        System.out.println(c);
    }
}
